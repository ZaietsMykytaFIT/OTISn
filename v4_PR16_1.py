import numpy as np              # –ó–æ–±—Ä–∞–∑–∏—Ç–∏ –≥—Ä–∞—Ñ—ñ–∫ —Ñ—É–Ω–∫—Ü—ñ—ó ùë¶=0,5ùë•+0,1ùë•‡¨∏, ùë•‚àà[‚àí3;9],
import matplotlib.pyplot as plt # —Ç–∏–ø –ª—ñ–Ω—ñ—ó ‚Äì –ø—É–Ω–∫—Ç–∏—Ä–Ω–∞, –∫–æ–ª—ñ—Ä –ª—ñ–Ω—ñ—ó ‚Äì —á–æ—Ä–Ω–∏–π. –ó–±–µ—Ä–µ–≥—Ç–∏ –≥—Ä–∞—Ñ—ñ–∫ —É —Ñ–∞–π–ª gr_file.tiff.


